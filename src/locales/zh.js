/**
 * ============================================
 * 多语言支持说明
 * ============================================
 * 
 * 当前项目的多语言支持非常有限：
 * 
 * 1. 已支持：
 *    - 音频分类标签：通过本文件配置
 *    - 音频按钮名称：在 voices.js 的 messages 中配置
 * 
 * 2. 不支持（需要修改代码）：
 *    - 界面按钮文字（随机播放、停止所有等）
 *    - 标题文字
 *    - 提示信息
 *    - CDN选择界面文字
 *    - 加载界面文字
 * 
 * 3. 扩展多语言支持的步骤：
 *    (1) 创建对应语言的文件，如 en.js、ja.js
 *    (2) 在 main.js 中导入并添加到 locales 对象
 *    (3) 修改界面渲染代码，使用 state.currentLang 获取对应翻译
 * 
 * ============================================
 * 使用示例
 * ============================================
 * 
 * 1. 在 voices.js 中：
 *    tag: "greetings"  →  需要在本文件中定义 "greetings" 的翻译
 * 
 * 2. 系统会自动查找：
 *    - 当前语言是 "zh" 时，显示 "问候语"
 *    - 如果没有对应翻译，显示原 tag 值 "greetings"
 * 
 * 3. 新增分类的步骤：
 *    (1) 在 voices.js 中给音频设置新的 tag
 *    (2) 在本文件中添加对应的中文翻译
 *    (3) 不需要修改其他代码，系统会自动分组显示
 * 
 * ============================================
 * 常见问题
 * ============================================
 * 
 * Q: 为什么我的分类显示为英文而不是中文？
 * A: 检查 tag 拼写是否完全一致，包括大小写
 * 
 * Q: 如何添加新的语言支持？
 * A: 需要修改主代码，目前只支持中文显示
 * 
 * Q: 音频按钮名称可以显示不同语言吗？
 * A: 可以，在 voices.js 的 messages 中配置多语言名称即可
 */